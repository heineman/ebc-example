package oo.contractChecking;

public interface IDuplicate {

	/**
	 * Given two arrays without duplicate integers, produce an 
	 * array that contains those items found in both a[] and b[]
	 * 
	 * @param    a is an array of unique ints.
	 * @param    b is an array of unique ints.
	 *  
	 * @return   array of duplicates found in both a and b
	 * @exception  
	 */
	public int[] compare (int [] a, int [] b) throws Exception;
}
